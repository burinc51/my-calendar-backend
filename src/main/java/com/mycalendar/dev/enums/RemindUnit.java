package com.mycalendar.dev.enums;

/**
 * Unit of time used for the remindBefore notification offset.
 */
public enum RemindUnit {
    MINUTES,    // e.g. remind 15 minutes before
    HOURS,      // e.g. remind 2 hours before
    DAYS,       // e.g. remind 1 day before
    WEEKS       // e.g. remind 1 week before
}

